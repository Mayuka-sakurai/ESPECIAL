package com.especial.VO;

public class ReviewCount {
	private int hotel;
	private int facility;
	private int offer;
	private int wedding_Conference;
	
	
	public int getHotel() {
		return hotel;
	}
	public void setHotel(int hotel) {
		this.hotel = hotel;
	}
	public int getFacility() {
		return facility;
	}
	public void setFacility(int facility) {
		this.facility = facility;
	}
	public int getOffer() {
		return offer;
	}
	public void setOffer(int offer) {
		this.offer = offer;
	}
	public int getWedding_Conference() {
		return wedding_Conference;
	}
	public void setWedding_Conference(int wedding_Conference) {
		this.wedding_Conference = wedding_Conference;
	}
	
	
}
